package reflect;

public class Reflect {
	  private String name;  
	    private String psw;  
	      
	    public void setName(String name){  
	        this.name=name;  
	    }  
	       
	    public String getName(){  
	        return name;  
	    }  
	      
	    public void setPsw(String name){  
	        this.psw=psw;  
	    }  
	    public String getPsw(){  
	        return psw;  
	    }  
}
